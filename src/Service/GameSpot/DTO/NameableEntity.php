namespace App\Service\GameSpot\DTO;

class NameableEntity implements DTO
{
    /**
     * @var string
     */
    private $name;

    /**
     * NameableEntity constructor.
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
